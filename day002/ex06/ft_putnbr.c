void	ft_putchar(char c);

void	ft_putnbr(int nb)
{
	int		temp;
	char	ctemp;

	if (nb < 0)
	{
		ft_putchar('-');
		nb *= -1;
	}
	while (nb > 0)
	{
		temp = nb % 10;
		ctemp = '0' + temp;
		nb /= 10;
	}
}
