#include <stdio.h>

int	ft_strncmp(char *s1, char *s2, int n)
{
	int	i;

	i = 0;
	printf("N: %i", n);
	while (s1[i] != '\0' && s2[i] != '\0' && i < n)
	{
		printf("S1:%c, S2: %c, i: %d\n", s1[i], s2[i], i);
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	return (0);
}
